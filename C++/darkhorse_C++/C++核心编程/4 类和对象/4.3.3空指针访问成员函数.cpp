#include<iostream>
using namespace std;

class Person {
public:

	void ShowClassName() {
		cout << "我是Person类!" << endl;
	}

	void ShowPerson() {
		if (this == NULL) {
            cout<<"haha"<<endl;
			return;
		}
		cout << mAge << endl;
	}

public:
	int mAge;
};

void test01()
{
	// Person * p ;
    Person*p=NULL;
	p->ShowClassName(); //空指针，可以调用成员函数
	p->ShowPerson();  //但是如果成员函数中用到了this指针，就不可以了
}

int main() {

	test01();

	system("pause");

	return 0;
}